ZVox = ZVox or {}

local function checkerFunc(x, y, r, g, b)
	if (x + y) % 2 == 0 then
		local mul = .85
		return r * mul, g * mul, b * mul
	else
		return r, g, b
	end
end

function ZVox.ComputeDebugTextures()
	-- X
	ZVox.NewTexturePixelFunc("plus_x", function(x, y)
		return checkerFunc(x, y, 255, 96, 64)
	end)
	ZVox.TextureHexWriteOp("plus_x", {0x0, 0x0, 0x0, 0x0, 0x44, 0x844, 0x828, 0x3e10, 0x828, 0x844, 0x44, 0x0, 0x0, 0x0, 0x0, 0x0, }, 255, 255, 255)

	ZVox.NewTexturePixelFunc("minus_x", function(x, y)
		return checkerFunc(x, y, 255, 96, 64)
	end)
	ZVox.TextureHexWriteOp("minus_x", {0x0, 0x0, 0x0, 0x0, 0x44, 0x44, 0x28, 0x3e10, 0x28, 0x44, 0x44, 0x0, 0x0, 0x0, 0x0, 0x0, }, 255, 255, 255)

	-- Y
	ZVox.NewTexturePixelFunc("plus_y", function(x, y)
		return checkerFunc(x, y, 64, 255, 96)
	end)
	ZVox.TextureHexWriteOp("plus_y", {0x0, 0x0, 0x0, 0x0, 0x44, 0x844, 0x828, 0x3e10, 0x810, 0x810, 0x10, 0x0, 0x0, 0x0, 0x0, 0x0, }, 255, 255, 255)

	ZVox.NewTexturePixelFunc("minus_y", function(x, y)
		return checkerFunc(x, y, 64, 255, 96)
	end)
	ZVox.TextureHexWriteOp("minus_y", {0x0, 0x0, 0x0, 0x0, 0x44, 0x44, 0x28, 0x3e10, 0x10, 0x10, 0x10, 0x0, 0x0, 0x0, 0x0, 0x0, }, 255, 255, 255)

	-- Z
	ZVox.NewTexturePixelFunc("plus_z", function(x, y)
		return checkerFunc(x, y, 96, 64, 255)
	end)
	ZVox.TextureHexWriteOp("plus_z", {0x0, 0x0, 0x0, 0x0, 0x7c, 0x804, 0x808, 0x3e10, 0x820, 0x840, 0x7c, 0x0, 0x0, 0x0, 0x0, 0x0, }, 255, 255, 255)

	ZVox.NewTexturePixelFunc("minus_z", function(x, y)
		return checkerFunc(x, y, 96, 64, 255)
	end)
	ZVox.TextureHexWriteOp("minus_z", {0x0, 0x0, 0x0, 0x0, 0x7c, 0x4, 0x8, 0x3e10, 0x20, 0x40, 0x7c, 0x0, 0x0, 0x0, 0x0, 0x0, }, 255, 255, 255)




	ZVox.NewTexturePixelFunc("nodraw", function(x, y)
		return checkerFunc(x, y, 244, 216, 36)
	end)
	ZVox.TextureHexWriteOp("nodraw", {0x0, 0x0, 0x0, 0x1278, 0x1a48, 0x1648, 0x1278, 0x0, 0x0, 0x7392, 0x4a52, 0x4b96, 0x725e, 0x0, 0x0, 0x0, }, 0, 0, 0)

	ZVox.NewTexturePixelFunc("air", function(x, y)
		return checkerFunc(x, y, 255, 128, 64)
	end)
	ZVox.TextureHexWriteOp("air", {0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x7bdc, 0x4992, 0x799c, 0x4bd2, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, }, 0, 0, 0)
end