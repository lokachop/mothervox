ZVox = ZVox or {}

local function guessNormalFromNeighbours(xOrigin, yOrigin, hexWrite)
	local didCatch = false
	local dirAccum = Vector(0, 0, 0)

	for y = -1, 1 do
		for x = -1, 1 do
			local cont = ZVox.SampleHexWrite(xOrigin + x, yOrigin + y, hexWrite)
			if not cont then
				continue
			end

			didCatch = true

			local dir = Vector(x, y, 0)
			dir:Normalize()

			dirAccum:Add(dir)
		end
	end

	dirAccum:Normalize()
	--dirAccum = dirAccum / maxCapDiv
	return didCatch, dirAccum
end

local function inrange_exclusive(x, a, b)
	return x >= a and x < b
end

local function isTextureBorder_Width(x, y, width)
	width = width or 1
	return (inrange_exclusive(x, 0, width) or inrange_exclusive(x, 16 - width, 16)) or (inrange_exclusive(y, 0, width) or inrange_exclusive(y, 16 - width, 16))
end

local function normDistTo(x, y, tX, tY)
	local diffX = math.abs(x - tX)
	diffX = diffX / 16

	local diffY = math.abs(y - tY)
	diffY = diffY / 16

	return (diffX + diffY) / 2
end


function ZVox.NewTextureNumberBlock(name, hexWrite)
	ZVox.NewTexturePixelFunc(name, function(x, y)
		if isTextureBorder_Width(x, y, 1) then
			local deltaCalc = normDistTo(x, y, 0, 0)

			local colLerp = ZVox.LerpColor(deltaCalc, Color(267, 267, 320), Color(156, 156, 188))

			return colLerp.r - 48, colLerp.g - 48, colLerp.b - 48
		end

		local val = .8 + (math.random() * .2)

		return 64 * val, 48 * val, 24 * val
	end)

	ZVox.TextureOpPixelFunc(name, function(x, y)
		if ZVox.SampleHexWrite(x, y, hexWrite) then
			return 255, 196, 0, 255
		end
	end)

	ZVox.SetTextureEmissive(name, true)
end

