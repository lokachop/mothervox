ZVox = ZVox or {}
ZVUI = ZVUI or {}
local ICON_ATLAS_SZ = 128
local ICON_ATLAS_ICONS_PER_AXIS = math.floor(ICON_ATLAS_SZ / 16)
local ICON_ATLAS_UV_SZ = 16 / ICON_ATLAS_SZ

local _iconRegistry = {}
local _iconIDToName = {}


--local ICON_NO

local lastIdx = 0
function ZVUI.NewIconEx(name, multiData)
	lastIdx = lastIdx + 1

	_iconRegistry[name] = {
		["id"] = lastIdx,
		["name"] = name,
		["multiData"] = multiData,
		["u"] = 0,
		["v"] = 0,
	}

	_iconIDToName[lastIdx] = name
end

function ZVUI.NewIcon(name, hexData) -- pure white icon
	local mData = {
		{Color(255, 255, 255), hexData}
	}

	ZVUI.NewIconEx(name, mData)
end

function ZVUI.NewIconMultiData(name, multiData) -- colourful icons with more stages
	ZVUI.NewIconEx(name, multiData)
end

-- this is in a 2d context
function ZVUI.ComputeIcon(hexData, x, y, r, g, b, a)
	a = a or 255

	local oW, oH = ScrW(), ScrH()
	for i = 0, (16 * 16) - 1 do
		local xc = i % 16
		local yc = math.floor(i / 16)
		local row = hexData[yc + 1]
		if not row then
			continue
		end


		local xcBand = 15 - xc

		local bitGet = bit.band(row, 2^xcBand)

		if bitGet == 0 then
			continue
		end

		render.SetViewPort(xc + x, yc + y, 1, 1)
		render.Clear(r, g, b, a)
	end
	render.SetViewPort(0, 0, oW, oH)
end

local iconAtlasRT = GetRenderTarget("zvox_icon_atlas_rt", ICON_ATLAS_SZ, ICON_ATLAS_SZ)
local iconAtlasMat = CreateMaterial("zvox_icon_atlas_mat", "UnlitGeneric", {
	["$basetexture"] = iconAtlasRT:GetName(),
	["$vertexcolor"] = 1,
	["$vertexalpha"] = 1,
})

function ZVUI.RecomputeIconAtlas()
	ZVox.RenderOnRT(iconAtlasRT, function()
		render.Clear(0, 0, 0, 0, true, true)

		for i = 1, lastIdx do
			local idxPos = i - 1


			local icon = _iconRegistry[_iconIDToName[i]]

			local xc = idxPos % ICON_ATLAS_ICONS_PER_AXIS
			local yc = math.floor(idxPos / ICON_ATLAS_ICONS_PER_AXIS)


			local rX = xc * 16
			local rY = yc * 16

			icon["u"] = rX / ICON_ATLAS_SZ
			icon["v"] = rY / ICON_ATLAS_SZ

			local mData = icon.multiData

			for j = 1, #mData do
				local dataVar = mData[j]

				local colData = dataVar[1]
				local hexData = dataVar[2]
				ZVUI.ComputeIcon(hexData, rX, rY, colData.r, colData.g, colData.b, colData.a)
			end
		end
	end)
end

local function uvCorrect(u0, v0, u1, v1)
	--local du = 0.5 / 32 -- half pixel anticorrection
	--local dv = 0.5 / 32 -- half pixel anticorrection
	--u0, v0 = (u0 - du) / (1 - 2 * du), (v0 - dv) / (1 - 2 * dv)
	--u1, v1 = (u1 - du) / (1 - 2 * du), (v1 - dv) / (1 - 2 * dv)

	return u0, v0, u1, v1
end


-- the icons are in an atlas to not make 9999 RTs so we can't give specific textures
function ZVUI.RenderIcon(name, x, y, w, h)
	local iconData = _iconRegistry[name]
	if not iconData then
		return
	end

	w = w or 16
	h = h or 16

	local uOrigin = iconData.u
	local vOrigin = iconData.v

	local uEnd = uOrigin + ICON_ATLAS_UV_SZ
	local vEnd = vOrigin + ICON_ATLAS_UV_SZ

	render.PushFilterMag(TEXFILTER.POINT)
	render.PushFilterMin(TEXFILTER.POINT)

		surface.SetMaterial(iconAtlasMat)
		surface.DrawTexturedRectUV(x, y, w, h, uvCorrect(uOrigin, vOrigin, uEnd, vEnd))

	render.PopFilterMag()
	render.PopFilterMin()
end


